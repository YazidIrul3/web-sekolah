const mongoose = require("mongoose");

const confirmNewsSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const confirmNewsModel = mongoose.model("confirmNews", confirmNewsSchema);

module.exports = confirmNewsModel;
